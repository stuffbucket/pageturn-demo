#!/usr/bin/env node
/**
 * gen-fiducial-sweep.mjs — generate 280 deterministic harness scenarios
 * that drive a single drag from each of the 5x7 fiducials on each page of
 * a two-page spread, in each of 4 cardinal directions (±x, ±y).
 *
 * Output: harness/scenarios/fiducial-grid-sweep/<page>-<i>-<j>-<dir>.json
 *
 * Naming:  fid-r-3-2-px.json    = right page, fiducial (i=3, j=2), drag +x
 *          fid-l-1-4-my.json    = left  page, fiducial (i=1, j=4), drag -y
 *
 * Layout assumptions (mirror harness/scenarios/horizontal-pull.json):
 *   - Book spans viewport x ≈ [0.24, 0.76]; right half ⇒ forward turn,
 *     left half ⇒ reverse turn.
 *   - Viewport 640x360 @ 24fps; gestures last ~1s, then a 600ms settle pad.
 *
 * The generated scenarios cover the user's "lift the page from every
 * fiducial on each page of a two page spread including dragging the mouse
 * vertically and horizontally across fiducial rows and columns to observe
 * and capture behavior" ask in the diagnostic instrumentation task.
 *
 * Run:  node scripts/gen-fiducial-sweep.mjs
 */

import { mkdirSync, writeFileSync, rmSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT = resolve(__dirname, '..', 'harness', 'scenarios', 'fiducial-grid-sweep');

// Mirror src/textures/atlas.ts.
const FIDUCIAL_US = [0.1, 0.3, 0.5, 0.7, 0.9];
const FIDUCIAL_VS = [0.08, 0.22, 0.36, 0.50, 0.64, 0.78, 0.92];

// Viewport-space book layout: right page spans x∈[0.50, 0.76]; left page
// spans x∈[0.24, 0.50]. Vertical extent of the spread is y∈[0.20, 0.80].
const RIGHT_X0 = 0.50, RIGHT_X1 = 0.76;
const LEFT_X0  = 0.24, LEFT_X1  = 0.50;
const Y0       = 0.20, Y1       = 0.80;

// Map (page, fiducialU, fiducialV) → viewport (x, y).
function fidToViewport(page, u, v) {
  const x0 = page === 'r' ? RIGHT_X0 : LEFT_X0;
  const x1 = page === 'r' ? RIGHT_X1 : LEFT_X1;
  // For the right page, u=0 = spine (left edge), u=1 = free edge (right).
  // For the left page, u=0 = spine (right edge), u=1 = free edge (left) —
  // mirror the X mapping so a +x drag still drags toward the spine for both.
  const x = page === 'r'
    ? x0 + u * (x1 - x0)
    : x1 - u * (x1 - x0);
  // v=0 top of page, v=1 bottom — match the canvas-space orientation that
  // FIDUCIAL_VS uses in atlas.ts (small v = top of page).
  const y = Y0 + v * (Y1 - Y0);
  return { x, y };
}

const DRAG_AMPLITUDE = 0.18; // viewport units (~half page width)

function dirOffset(dir) {
  switch (dir) {
    case 'px': return { dx:  DRAG_AMPLITUDE, dy: 0 };
    case 'mx': return { dx: -DRAG_AMPLITUDE, dy: 0 };
    case 'py': return { dx: 0, dy:  DRAG_AMPLITUDE };
    case 'my': return { dx: 0, dy: -DRAG_AMPLITUDE };
    default:   throw new Error(`unknown dir ${dir}`);
  }
}

function genEvents(start, dir) {
  const { dx, dy } = dirOffset(dir);
  const STEP = 4;
  const events = [];
  // Pre-roll: move the pointer to the origin without pressing.
  events.push({ t: 200, type: 'pointerdown', x: start.x, y: start.y });
  for (let s = 1; s <= STEP; s++) {
    const u = s / STEP;
    events.push({
      t: 200 + s * 220,
      type: 'pointermove',
      x: clamp(start.x + dx * u, 0.02, 0.98),
      y: clamp(start.y + dy * u, 0.02, 0.98),
    });
  }
  events.push({
    t: 200 + (STEP + 1) * 220,
    type: 'pointerup',
    x: clamp(start.x + dx, 0.02, 0.98),
    y: clamp(start.y + dy, 0.02, 0.98),
  });
  return events;
}

function clamp(v, lo, hi) { return Math.max(lo, Math.min(hi, v)); }

function makeScenario(page, i, j, dir) {
  const start = fidToViewport(page, FIDUCIAL_US[i], FIDUCIAL_VS[j]);
  const events = genEvents(start, dir);
  const lastT = events[events.length - 1].t;
  return {
    name: `fid-${page}-${i}-${j}-${dir}`,
    viewport: { width: 640, height: 360 },
    duration: lastT + 1500,
    fps: 24,
    _notes: `Fiducial-grid sweep: page=${page} fiducial=(i=${i},j=${j},u=${FIDUCIAL_US[i]},v=${FIDUCIAL_VS[j]}) dir=${dir}. Auto-generated by scripts/gen-fiducial-sweep.mjs (see docs/diagnostic-2026-05-14.md). Pair with the analytic heat-map in scripts/fiducial-heatmap.mjs to detect FR-P1 violations that depend on drag-origin location.`,
    events,
  };
}

function main() {
  if (existsSync(OUT)) rmSync(OUT, { recursive: true, force: true });
  mkdirSync(OUT, { recursive: true });

  const dirs = ['px', 'mx', 'py', 'my'];
  let count = 0;
  for (const page of ['r', 'l']) {
    for (let i = 0; i < FIDUCIAL_US.length; i++) {
      for (let j = 0; j < FIDUCIAL_VS.length; j++) {
        for (const dir of dirs) {
          const s = makeScenario(page, i, j, dir);
          writeFileSync(resolve(OUT, `${s.name}.json`), JSON.stringify(s, null, 2) + '\n');
          count++;
        }
      }
    }
  }
  console.log(`Wrote ${count} scenarios to ${OUT}`);
}

main();
